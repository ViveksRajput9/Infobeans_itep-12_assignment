public class demo {
    public static void main(String[] args) {
        for (int k =4;k<=(k*2)-1;k--) {
            for(int j = 1;j<=k;j++){
                System.out.print(" ");
            }
            for(int  a =1;a<k;a++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}